// RGB7004 - Куб
// Өгөгдсөн кубын эзэлхүүн ба гүйцэд гадаргуугийн талбайг ол.

// Input
// Кубын тал бүхэл тоон утгатайгаар нэг мөрөнд өгөгдөнө.

// Output
// Кубын эзэлхүүн, гүйцэд гадаргуугийн талбай нэг мөрөнд зайгаар тусгаарлагдан хэвлэгдэнэ.

// Example
// Input:
// 5

// Output:
// 125 150

function cube(a) {
  v = a * a * a;
  s = a * a * 6;
  console.log(v, "=", a, "*", a, "*", a);
  console.log(s, "=", "(", a, "*", a, ")", "*", "6");
}
cube(5);
