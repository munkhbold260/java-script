// Өгөгдсөн n тоо хүртэлх квадратуудын нийлбэрийг ол. Давталт ашиглахгүй болно.

// 12+22+...+n2

// Input
// n натурал тоо өгөгдөнө.

// Output
// Нийлбэр

// Example
// Input:
// 5

// Output:
// 55

let sum = 0;
function num(x) {
  sum = (x * (x + 1) * (2 * x + 1)) / 6;
}
num(5);
console.log(sum);
