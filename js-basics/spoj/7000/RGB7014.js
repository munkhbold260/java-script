// RGB7014 - Цаг, минут, секунд -> секунд
// Өгөгдсөн цаг, минут, секундыг секунд рүү шилжүүл.

// Input
// Цаг, минут, секунд зайгаар тусгаарлагдан нэг мөрөнд Int төрлөөр өгөгдөнө.

// Output
// Секунд гарна.

// Example
// Input:
// 1 2 3
// Output:
// 3723

function time(a, b, c) {
  sec = a * 3600 + b * 60 + c;
  console.log(sec);
}
time(1, 2, 3);
