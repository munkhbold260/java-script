// Өгөгдсөн тоог тодорхой давталт ашиглан өгөгдсөн тоон удаа нэм.

// Input
// Нэг мөрөнд Int төрлийн 2 тоо зайгаар тусгаарлагдан өгөгдөнө. Эхний тоо нь a тоо буюу тойрог тутамд нэмэгдэх тоо. 2 дахь тоо нь тойргийн тоо.

// Output
// Нийлбэр.

// Example
// Input:

// 50 6

// Output:

// 300

function math(a, b) {
  let sum = 0;
  for (let i = 1; i <= b; i = i + 1) {
    sum = sum + a;
    console.log(sum);
  }
}

math(50, 9);
