// IOI үгийг өгөгдсөн тоон удаа хэвлэ.

// Input
// Int төрлийн эерэг бүхэл тоо өгөгдөнө.

// Output
// Мөр тус бүрт нэг IOI үг бичнэ.

// Example
// Input:
// 4

// Output:
// IOI
// IOI
// IOI
// IOI

function printIOI(n) {
  for (let i = 0; i < n; i = i + 1) {
    console.log("IOI");
  }
}
printIOI(9);
