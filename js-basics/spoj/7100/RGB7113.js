// RGB7113 - 5-аас бага
// Өгөгдсөн тоо 5-аас бага бол  YES үгүй бол NO гэж хэвлэ.

// Input
// Int төрлийн нэг тоо өгөгдөнө.

// Output
// YES эсвэл NO гэж хэвлэгдэнэ.

// Example
// Input:
// 4
// Output:
// YES

let a = 4;
if (a < 5) {
  console.log("yes");
} else console.log("no");

function math(b) {
  if (b > 5) {
    console.log("yes");
  } else console.log("no");
}
math(6);
