// RGB7112 - 10-аас их
// Өгөгдсөн тоо 10-аас их бол YES үгүй бол NO гэж хэвлэ.

// Input
// Int төрлийн 1 тоо өгөгдөнө.

// Output
// YES эсвэл NO гэж хэвлэгдэнэ.

// Example
// Input:
// 11

// Output:
// YES

function math(a) {
  if (a > 10) {
    console.log("yes");
  } else console.log("no");
}
math(9);
