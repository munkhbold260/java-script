// RGB7116 - 5-аас онц руу
// Өгөгдсөн тоон дүнг үсгэн дүн рүү шилжүүл.

// Input
// Тоон дүн нь 2,3,4,5-ын зөвхөн аль нэгээр өгөгдөнө.

// Output
// 5 байвал "Onts", 4 байвал "Sain", 3 байвал "Dund", 2 байвал "Muu" гэж хэвлэ.

// Example
// Input:
// 4
// Output:
// Sain
let dun = 4;
if (dun == 2) {
  console.log("muu");
} else if (dun == 3) {
  console.log("dund");
} else if (dun == 4) {
  console.log("sain");
} else console.log("onts");
